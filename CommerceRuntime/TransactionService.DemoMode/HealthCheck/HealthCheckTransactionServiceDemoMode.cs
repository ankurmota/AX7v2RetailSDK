        /// Implementation for health check demo mode transaction service.
        /// </summary>
        public class HealthCheckTransactionServiceDemoMode : IRequestHandler
        {
            /// <summary>
            /// Gets the collection of supported request types by this handler.
            /// </summary>
            public IEnumerable<Type> SupportedRequestTypes
            {
                get
                {
                    return new[]
                    {
                        typeof(RunHealthCheckRealtimeRequest),
                    };
                }
            }
    
            /// <summary>
            /// Represents the entry point of the request handler.
            /// </summary>
            /// <param name="request">The incoming request message.</param>
            /// <returns>The outgoing response message.</returns>
            public Response Execute(Request request)
            {
                if (request == null)
                {
                    throw new ArgumentNullException("request");
                }
    
                Type requestType = request.GetType();
                Response response;
    
                if (requestType == typeof(RunHealthCheckRealtimeRequest))
                {
                    response = RunHealthCheck((RunHealthCheckRealtimeRequest)request);
                }
                else
                {
                    throw new NotSupportedException(string.Format(CultureInfo.InvariantCulture, "Request type '{0}' is not supported.", request.GetType()));
                }
    
                return response;
            }
    
            private static RunHealthCheckRealtimeResponse RunHealthCheck(RunHealthCheckRealtimeRequest request)
            {
                ThrowIf.Null(request, "request");
    
                return new RunHealthCheckRealtimeResponse(true);
            }
        }
