namespace Contoso
{
    namespace Commerce.Runtime.Workflow.Security
    {
        using Microsoft.Dynamics.Commerce.Runtime;
        using Microsoft.Dynamics.Commerce.Runtime.DataModel;
        using Microsoft.Dynamics.Commerce.Runtime.Messages;
        using Microsoft.Dynamics.Commerce.Runtime.Services.Messages;

        /// <summary>
        /// The request handler for <see cref="StartSessionRequest"/>.
        /// </summary>
        public class StartSessionRequestHandler : SingleRequestHandler<StartSessionRequest, NullResponse>
        {
            /// <summary>
            /// Represents the entry point of the request handler.
            /// </summary>
            /// <param name="request">The incoming request message.</param>
            /// <returns>The outgoing response message.</returns>
            protected override NullResponse Process(StartSessionRequest request)
            {
                ThrowIf.Null(request, "request");

                // creates a new session
                request.RequestContext.Execute<NullResponse>(new CreateStaffSessionServiceRequest());

                AuthenticationHelper.LogTransaction(request.RequestContext, TransactionType.LogOn, request.TransactionId);

                return new NullResponse();
            }
        }
    }
